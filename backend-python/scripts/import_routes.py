async def import_routes():
    """将路线模板导入 ChromaDB"""
    for route in ROUTES:
        doc_id = route["id"]
        title = route["title"]
        content = route["content"]

        # 按段落切分
        paragraphs = [p.strip() for p in content.split("\n\n") if p.strip()]
        chunks = []
        for i, para in enumerate(paragraphs):
            chunks.append({"text": para, "index": i})

        print(f"Importing route: {title} ({len(chunks)} chunks)")

        # Embedding
        texts = [c["text"] for c in chunks]
        embeddings = await embed_texts(texts)

        # 存入 ChromaDB
        add_document_chunks(doc_id, title, chunks, embeddings)
        print(f"  Done: {title}")

    print(f"\nAll {len(ROUTES)} routes imported!")


if __name__ == "__main__":
    asyncio.run(import_routes())
